package org.example.model;

/**
 * Enum che rappresenta lo stato di completamento di una voce della checklist.
 */
public enum StatoCheck {
    /**
     * Stato che indica che la voce della checklist non è ancora completata.
     */
    INCOMPLETO,

    /**
     * Stato che indica che la voce della checklist è stata completata.
     */
    COMPLETATO
}


